package defs;

public class Errors {
	public double topicUser;
	public double topicItem;
	public double brandUser;
	public double brandItem;
	public double decisionPrefs;
	
	public Errors(double topicUser, double topicItem, double brandUser, double brandItem, double decisionPrefs) {
		super();
		this.topicUser = topicUser;
		this.topicItem = topicItem;
		this.brandUser = brandUser;
		this.brandItem = brandItem;
		this.decisionPrefs = decisionPrefs;
	}
}
